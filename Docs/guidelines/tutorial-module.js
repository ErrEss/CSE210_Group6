/**
 * Calculator module
 * @module calculator
 */

/**
 * Add two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @return {number} - Sum of n1 and n2
 */
exports.add = (n1, n2) => n1 + n2;

/**
 * Multiply two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @return {number} - Product of n1 and n2
 */
exports.multiply = (n1, n2) => n1 * n2;

/**
 * Subtract two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @return {number} - Difference of n1 and n2
 */
exports.subtract = (n1, n2) => n1 - n2;

/**
 * Divide two numbers
 * @param {number} n1 - First number
 * @param {number} n2 - Second number
 * @return {number} - Quotient of n1 and n2
 */
exports.divide = (n1, n2) => n1 / n2;
